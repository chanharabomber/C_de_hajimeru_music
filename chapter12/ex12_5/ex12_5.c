#include <stdio.h>
#include <stdlib.h>
#include <math.h>
#include "wave.h"

int main(void)
{
  MONO_PCM pcm0, pcm1;
  int n;
  double a, depth, rate;
  
  mono_wave_read(&pcm0, "sample12.wav"); /* WAVEファイルからモノラルの音データを入力する */
  
  pcm1.fs = pcm0.fs; /* 標本化周波数 */
  pcm1.bits = pcm0.bits; /* 量子化精度 */
  pcm1.length = pcm0.length; /* 音データの長さ */
  pcm1.s = calloc(pcm1.length, sizeof(double)); /* メモリの確保 */
  
  depth = 1.0;
  rate = 150.0; /* 150Hz */
  
  /* ボイスチェンジャ */
  for (n = 0; n < pcm1.length; n++)
  {
    a = depth * sin(2.0 * M_PI * rate * n / pcm1.fs);
    pcm1.s[n] = a * pcm0.s[n];
  }
  
  mono_wave_write(&pcm1, "ex12_5.wav"); /* WAVEファイルにモノラルの音データを出力する */
  
  free(pcm0.s); /* メモリの解放 */
  free(pcm1.s); /* メモリの解放 */
  
  return 0;
}
